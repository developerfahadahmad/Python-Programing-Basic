# Comparison operators

# > , < , == , <= , >= , !=

x,y = 10 , 20


print(x == y)       # False
print(x > y)        # False
print(x < y)        # True
print(x != y)       # True

a,b = 40,40

print(a == b)       # True
print(a >= b)       # True
print(a <= b)       # True
print(a != b)       # False